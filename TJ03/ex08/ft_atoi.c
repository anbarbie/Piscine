int		ft_atoi(char *str)
{
	int	res = 0;
	int	i = 0;
	int	sign = 1;

	while (str [i] == '\n' ||
			str [i] == '\t' ||
			str [i] == ' ' ||
			str [i] == '\v' ||
			str [i] == '\f' ||
			str [i] == '\r')
		i++;
	if (str [i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str [i] == '+')
		i++;
	while (*(str + i) >= 48 && *(str + i) <= 57)
	{
		res = res * 10 + *(str + i) - 48;
		i++;
	}
	return (sign*res);
}
