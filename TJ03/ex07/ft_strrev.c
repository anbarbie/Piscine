char	*ft_strrev(char *str)
{
	int i;
	int j;
	int tmp;

	j = 0;
	i = 0;
	while (str[i] != '\0')
		i++;
	i = i - 1;
	while (j < i)
	{
		tmp = str[j];
		str[j] = str[i];
		str[i] = tmp;
		i--;
		j++;
	}
	return (str);
}
